<script>
export default {
  name: "lateralSection",
  data() {
    return {
      brainParts: [
        "temporalLobe",
        "lateralSulcus",
        "midTemporalGyrus",
        "midbrain",
        "frontalLobe",
        "prefrontalLobe",
        "wernix",
        "broca",
        "motorCortex",
        "somatosensoryCortex",
        "postcentralGyrus",
        "preoccipitalNotch",
        "centralSulcus",
        "cerebellum",
      ],
      isClicked: false,
      currentQuestion: null,
      currentAnswer: null,
      questionsArray: ["DE FAK IZ DIS", "NU FON WHO DIS", "Ha?", "What is this?"],
      userAnswer: null,
      answerStatus: null,
      answerColor: "#e32d3f",
      answerLen: 0,
      rights: [],
      wrongs: []
    }
  },
  mounted() {
    const HOVER_COLOR = "#ff757e"
    const CHOSEN_COLOR = "#e96e5b"

    this.brainParts.forEach((part) => {
      console.log(part)
      let curr = document.querySelector(`#${part}`);
      console.log(curr)
      let clicked = ""
      curr.addEventListener("mouseover", function () {
        if (clicked !== part) {
          curr.style.fill = HOVER_COLOR
        }
      })
      curr.addEventListener("mouseout", function () {
        if (clicked !== part) {
          curr.style.fill = "rgba(0, 0, 0, 0)";
        }
      })
      curr.addEventListener("click", function () {
        if (clicked === part) {
          curr.style.fill = "rgba(0, 0, 0, 0)"
          clicked = ""
        } else {
          clicked = part
          curr.style.fill = CHOSEN_COLOR
        }
      })
    })
  },
  methods: {
    clickEvent(elem) {
      this.isClicked = true
      this.currentQuestion = this.questionsArray[Math.floor(Math.random() * this.questionsArray.length)]
      this.currentAnswer = elem
    },
    checkAnswer() {
      document.querySelector(".question-card").style.backgroundColor = this.answerColor;
      let curr = document.querySelectorAll(`.${this.currentAnswer}`)
      console.log(curr)
      if (this.answerColor === "#b2fcbb") {
        this.rights.push(this.currentAnswer);
        curr.forEach((item) => {
          item.removeEventListener("click", function () {
          }, false);
          item.removeEventListener("mouseover", function () {
          }, false);
          item.removeEventListener("mouseout", function () {
          }, false);
          item.style.fill = "#b2fcbb";
          item.classList.add("trueInput");
        })
      } else {
        this.wrongs.push(this.currentAnswer)
        curr.forEach((item) => {
          item.removeEventListener("click", function () {
          }, false);
          item.removeEventListener("mouseover", function () {
          }, false);
          item.removeEventListener("mouseout", function () {
          }, false);
          item.style.fill = "#e32d3f"
          item.classList.add("falseInput")
        })
      }
    }
  },
  watch: {
    userAnswer: {
      handler() {
        if (this.answerLen !== this.userAnswer.length) {
          document.querySelector(".question-card").style.backgroundColor = "#FFE3DC";
        }
        if (this.userAnswer.toLowerCase() === this.currentAnswer.toLowerCase()) {
          this.answerColor = "#b2fcbb"
        } else {
          this.answerColor = "#e32d3f"
        }
        this.answerLen = this.userAnswer.length
      }
    }
  }
}
</script>

